package TheaterListService;

public class TheaterListVO {

	private String show_name;
	private String show_startday;
	private String show_endday;
	private String concert_hall_code;
	private String show_code;
	
	
	
	public String getShow_name() {
		return show_name;
	}
	public void setShow_name(String show_name) {
		this.show_name = show_name;
	}
	public String getShow_startday() {
		return show_startday;
	}
	public void setShow_startday(String show_startday) {
		this.show_startday = show_startday;
	}
	public String getShow_endday() {
		return show_endday;
	}
	public void setShow_endday(String show_endday) {
		this.show_endday = show_endday;
	}
	public String getConcert_hall_code() {
		return concert_hall_code;
	}
	public void setConcert_hall_code(String concert_hall_code) {
		this.concert_hall_code = concert_hall_code;
	}
	public String getShow_code() {
		return show_code;
	}
	public void setShow_code(String show_code) {
		this.show_code = show_code;
	}
	
	
	
}
